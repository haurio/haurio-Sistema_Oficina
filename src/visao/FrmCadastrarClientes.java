    private void jTextFieldNomeFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldNomeFocusLost
        // Verificando qual é o tipo de pessoa. add 10/04/2018

        switch (jComboBoxTipoPess.getSelectedIndex()) {
            case 0:
                CPF_CNPJ.setText("CPF:");
                //JOptionPane.showMessageDialog(null, "PESSOA FISICA");
                break;
            case 1:
                CPF_CNPJ.setText("CNPJ:");
                jTextFieldRG.setEnabled(false);
                jComboBoxUF.setEnabled(false);
                jComboBoxCidade.setEnabled(false);
                jTextFieldIBGE.setEnabled(false);
                jTextFieldEndereco.setEnabled(false);
                jTextFieldBairro.setEnabled(false);
                jTextFieldRG.setText("00000");
                //JOptionPane.showMessageDialog(null, "PESSOA JURIDICA");
                break;
            default:
                JOptionPane.showMessageDialog(null, "Selecione o Tipo de Cliente");
        }
    }//GEN-LAST:event_jTextFieldNomeFocusLost

