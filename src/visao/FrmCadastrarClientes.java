    public void chamaIdCliente() {

        conectaClientes.conexao();

        try {
            conectaClientes.executaSQL("SELECT id_cliente FROM clientes");
            conectaClientes.rs.last();
            idCliente = conectaClientes.rs.getInt("id_cliente");
            jTextFieldCodigo.setText(String.valueOf(idCliente + 1));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Nenhum Cliente Cadastrado");
        } finally {

            conectaClientes.desconecta();

        }
    }
