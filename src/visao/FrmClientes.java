    private void chamaTabela(String SQL) {
       
        ArrayList dados = new ArrayList();
            
            String[] Colunas = new String[]{"Codigo Cliente","Nome Cliente", "Endereço", "Telefone", "Saldo Devedor", "Limite Compra"};
            conecta.conexao();
            conecta.executaSQL(SQL);
            
            try {
                
                conecta.rs.first();
                
                do {
                    float valor = conecta.rs.getFloat("saldo");
                    float resta = conecta.rs.getFloat("resta");
                    float total = valor - resta;
                    float limite = conecta.rs.getFloat("limite_compra");
                    String nome = conecta.rs.getString("nome_cliente");
                    dados.add(new Object[]{conecta.rs.getInt("id_cliente"), nome,conecta.rs.getString("endereco_cliente"),conecta.rs.getString("telefone"), dc.format(total), dc.format(limite)});
                } while (conecta.rs.next());
            } catch (Exception e) {
                
                
            }
            
            ModeloTabela modelo = new ModeloTabela(dados, Colunas);
            
            TabelaClientes.setModel(modelo);
            TabelaClientes.getColumnModel().getColumn(0).setPreferredWidth(120);
            TabelaClientes.getColumnModel().getColumn(0).setResizable(false);
            TabelaClientes.getColumnModel().getColumn(1).setPreferredWidth(400);
            TabelaClientes.getColumnModel().getColumn(1).setResizable(false);
            TabelaClientes.getColumnModel().getColumn(2).setPreferredWidth(280);
            TabelaClientes.getColumnModel().getColumn(2).setResizable(false);
            TabelaClientes.getColumnModel().getColumn(3).setPreferredWidth(180);
            TabelaClientes.getColumnModel().getColumn(3).setResizable(false);
            TabelaClientes.getColumnModel().getColumn(4).setPreferredWidth(180);
            TabelaClientes.getColumnModel().getColumn(4).setResizable(false);
            TabelaClientes.getColumnModel().getColumn(5).setPreferredWidth(180);
            TabelaClientes.getColumnModel().getColumn(5).setResizable(false);
           
            
            TabelaClientes.getTableHeader().setReorderingAllowed(false);
            TabelaClientes.setAutoResizeMode(TabelaClientes.AUTO_RESIZE_OFF);
            TabelaClientes.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            TabelaClientes.setRowHeight(30);
            TabelaClientes.removeAll();
            
            
        
    }
