    private void dadosClientes() {
        conecta.conexao();
        
        try {
            
            conecta.executaSQL("select count(id_cliente) as 'clientes' from clientes");
            if(conecta.rs.next()){
                
                conecta.rs.first();
                int qtClientes = conecta.rs.getInt("clientes");
                jTextFieldClientes.setText(String.valueOf(qtClientes));
                
            }
            
        } catch (Exception e) {
        }
        
        conecta.desconecta();
    }

