    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed

        if (jTextFieldCodigo.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo CODIGO esta sem preenchimento!");
            jTextFieldCodigo.requestFocus();

        } else if (jTextFieldDescricao.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo Descrição esta sem preenchimento!");
            jTextFieldDescricao.requestFocus();

        } else if (jTextFieldValor.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo Valor esta sem preenchimento!");
            jTextFieldValor.requestFocus();

        } else
            
            if (flag == 1) {

        mod.setCodigo(jTextFieldCodigo.getText());
        mod.setDescricacao(jTextFieldDescricao.getText());
        mod.setTipoServico((String)jComboBoxTipo.getSelectedItem());
        mod.setTempoServico((String)jComboBoxTempo.getSelectedItem());
        mod.setValorServico(Float.parseFloat(jTextFieldValor.getText().replace(",", ".")));
        
        
        Control.InserirServico(mod);
        preencherTabela("SELECT * FROM servicos  WHERE descricao LIKE '%" + jTextFieldPesquisa.getText() + "%' ORDER BY descricao ASC");
        
        Limparcampos();
            Limparcampos();
            BloquearCampos();
            jTextFieldPesquisa.setEnabled(true);
            jButtonCancelar.setEnabled(false);
            jButtonExcluir.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonNovo.setEnabled(true);
            
        } else {
                
        
        mod.setDescricacao(jTextFieldDescricao.getText());
        mod.setTipoServico((String)jComboBoxTipo.getSelectedItem());
        mod.setTempoServico((String)jComboBoxTempo.getSelectedItem());
        mod.setValorServico(Float.parseFloat(jTextFieldValor.getText().replace(",", ".")));
        mod.setCodigo(jTextFieldCodigo.getText());
        
        Control.AlterarServico(mod);
        preencherTabela("SELECT * FROM servicos  WHERE descricao LIKE '%" + jTextFieldPesquisa.getText() + "%' ORDER BY descricao ASC");
        }
        Limparcampos();
        BloquearCampos();
        jTextFieldPesquisa.setEnabled(true);
        jButtonCancelar.setEnabled(false);
        jButtonExcluir.setEnabled(false);
        jButtonSalvar.setEnabled(false);
        jButtonNovo.setEnabled(true);

    }//GEN-LAST:event_jButtonSalvarActionPerformed

