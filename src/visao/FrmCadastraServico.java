    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        String message = "Deseja Excluir O Serviço ?";
        String title = "Excluir Serviço";

        int result = JOptionPane.showConfirmDialog(this, message, title, JOptionPane.YES_OPTION);

        if (result == JOptionPane.YES_OPTION) {

            mod.setCodigo(jTextFieldCodigo.getText());

            Control.ExcluirServico(mod);

            preencherTabela("SELECT * FROM servicos  WHERE descricao LIKE '%" + jTextFieldPesquisa.getText() + "%' ORDER BY descricao ASC");

            BloquearCampos();
            Limparcampos();

            jButtonSalvar.setEnabled(false);
            jButtonNovo.setEnabled(true);
            jButtonCancelar.setEnabled(false);
            jButtonEditar.setEnabled(false);
            jButtonExcluir.setEnabled(false);

        } else {

            JOptionPane.showMessageDialog(null, "Produto não Excluido");
        
        }
     
    }//GEN-LAST:event_jButtonExcluirActionPerformed

