    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        jButtonSalvar.setEnabled(false);
        jButtonExcluir.setEnabled(true);
        jButtonEditar.setEnabled(true);
        jButtonNovo.setEnabled(false);
        jButtonCancelar.setEnabled(true);
        
        String servico = "" + tabela.getValueAt(tabela.getSelectedRow(), 1);
        conecta.conexao();
        conecta.conexao();
        conecta.executaSQL("SELECT * FROM servicos WHERE descricao ='" + servico + "'");

        try {
            conecta.rs.first();
            jTextFieldCodigo.setText(conecta.rs.getString("codigo"));
            jTextFieldDescricao.setText(conecta.rs.getString("descricao"));
            jComboBoxTipo.setSelectedItem(conecta.rs.getString("tipo_servico"));
            jComboBoxTempo.setSelectedItem(conecta.rs.getString("tempo_servico"));
            jTextFieldValor.setText(String.valueOf(df.format(conecta.rs.getFloat("valor_servico"))).replace(".", ","));
           

            //JOptionPane.showMessageDialog(null, servico);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao Selecionar os Dados" + ex);
        } finally {

            conecta.desconecta();
        }
    }//GEN-LAST:event_tabelaMouseClicked

