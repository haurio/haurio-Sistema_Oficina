    public void preencherTabela(String SQL) {

        ArrayList dados = new ArrayList();

        String[] Colunas = new String[]{"Código", "Descrição","Tipo", "Tempo","Valor"};
        conecta.conexao();
        conecta.executaSQL(SQL);

        try {

            conecta.rs.first();

            do {
                float valorservico = conecta.rs.getFloat("valor_servico");
                dados.add(new Object[]{conecta.rs.getString("codigo"), conecta.rs.getString("descricao"),conecta.rs.getString("tipo_servico"),
                    conecta.rs.getString("tempo_servico"), df.format(valorservico)});
            } while (conecta.rs.next());
            } catch (Exception e) {

             //JOptionPane.showMessageDialog(null, "ERRO AO CONECTAR"+ e);
        } finally {

            conecta.desconecta();
        }

        ModeloTabela modelo = new ModeloTabela(dados, Colunas);

        tabela.setModel(modelo);
        tabela.getColumnModel().getColumn(0).setPreferredWidth(195); // Codigo
        tabela.getColumnModel().getColumn(0).setResizable(false);
        tabela.getColumnModel().getColumn(1).setPreferredWidth(300); // Descrição
        tabela.getColumnModel().getColumn(1).setResizable(false);
        tabela.getColumnModel().getColumn(2).setPreferredWidth(150); // Tipo
        tabela.getColumnModel().getColumn(2).setResizable(false);
        tabela.getColumnModel().getColumn(3).setPreferredWidth(80); // Tempo
        tabela.getColumnModel().getColumn(3).setResizable(false);
        tabela.getColumnModel().getColumn(4).setPreferredWidth(80); // Valor
        tabela.getColumnModel().getColumn(4).setResizable(false);
       
      
        

        tabela.getTableHeader().setReorderingAllowed(false);
        tabela.setAutoResizeMode(tabela.AUTO_RESIZE_OFF);
        tabela.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tabela.setRowHeight(28);

    }

