    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jRadioButtonZerado.isSelected()){
       
            try {
                if(!conecta.rs.next()){
                    conecta.rs.first();
                    chamaTabela("select * from produto inner join categorias on produto.id_categoria = categorias.id_categoria where quantidade = 0");
                    
                }else{
                    
                    JOptionPane.showMessageDialog(null,"Não há Dados para essa Seleção");
                }
            } catch (SQLException ex) {
                Logger.getLogger(FrmProdutos.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        }else{
            try {
                if(!conecta.rs.next()){
                    conecta.rs.first();
                    chamaTabela("select * from produto inner join categorias on produto.id_categoria = categorias.id_categoria where quantidade_minima > quantidade");
            }else{
                    
                    JOptionPane.showMessageDialog(null,"Não há Dados para essa Seleção");
                }
            } catch (SQLException ex) {
                Logger.getLogger(FrmProdutos.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

