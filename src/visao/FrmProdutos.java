    private void qProduto() {
        conecta.conexao();
        conecta.executaSQL("select count(id_produto) as 'total', sum(quantidade) as 'quantidade', sum(preco_compra) as 'valorizado' from produto");
        try {
            conecta.rs.first();
            int total = conecta.rs.getInt("total");
            int qtTotal = conecta.rs.getInt("quantidade");
            float precoVenda = conecta.rs.getFloat("valorizado");
            jTextFieldTotal.setText(String.valueOf(total));
            jTextFieldTotalTotal.setText(String.valueOf(qtTotal));
            jTextFieldValorizado.setText(String.valueOf(dc.format(precoVenda)));
            
        } catch (SQLException ex) {
            
        }
        
    }

