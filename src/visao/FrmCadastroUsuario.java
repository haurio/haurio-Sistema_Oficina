    public void cadastrar(){
        if(!jTextFieldNome.getText().isEmpty()){
        
        mod.setUser(jTextFieldNome.getText());
        mod.setPass(jPasswordFieldSenha.getText());
        mod.setTipo((String)jComboBoxTipo.getSelectedItem());
        control.CadastrarUsuario(mod);
        }else{
        
            JOptionPane.showMessageDialog(null, "Preencher o Nome ");
            jTextFieldNome.requestFocus();
            
        
        }
        
    }
    public void limpar(){
        
        jTextFieldNome.setText("");
        jPasswordFieldSenha.setText("");
    }
