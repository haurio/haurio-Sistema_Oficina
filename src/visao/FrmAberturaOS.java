    private void TabelaClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaClienteMouseClicked

        String nome_cliente = "" + TabelaCliente.getValueAt(TabelaCliente.getSelectedRow(), 0);
        conecta.conexao();
        conecta.executaSQL("SELECT * FROM clientes WHERE id_cliente ='" + nome_cliente + "'");

        try {
            jComboBoxCliente.removeAllItems();
            jComboBoxCliente.removeAllItems();
            jComboBoxNome.removeAllItems();
            jComboBoxCPFCNPJ.removeAllItems();
            jComboBoxEndereco.removeAllItems();
            jComboBoxEmail.removeAllItems();
            jComboBoxtelefone.removeAllItems();

            do {
                conecta.rs.first();
                jTextFieldIDCliente.setText(String.valueOf(conecta.rs.getInt("id_cliente")));
                jComboBoxCliente.addItem(conecta.rs.getString("nome_cliente"));
                jComboBoxNome.addItem(conecta.rs.getString("nome_cliente"));
                jComboBoxCPFCNPJ.addItem(conecta.rs.getString("cpf_cliente"));
                jComboBoxEndereco.addItem(conecta.rs.getString("endereco_cliente"));
                jComboBoxEmail.addItem(conecta.rs.getString("email"));
                jComboBoxtelefone.addItem(conecta.rs.getString("telefone"));

            } while (conecta.rs.next());
        } catch (SQLException ex) {
            // JOptionPane.showMessageDialog(null, "Erro ao Selecionar os Dados" + ex);
        } finally {
            jTextFieldPesquisaCliente.setText("");
            PreencherTabelaVeiculos();

            conecta.desconecta();
        }
    }//GEN-LAST:event_TabelaClienteMouseClicked

