    public void preencherTabela(String SQL) {

        ArrayList dados = new ArrayList();

        String[] Colunas = new String[]{"ID", "Cliente", "CPF/CNPJ"};
        conecta.conexao();
        conecta.executaSQL(SQL);

        try {

            conecta.rs.first();

            do {

                dados.add(new Object[]{conecta.rs.getInt("id_cliente"), conecta.rs.getString("nome_cliente"), conecta.rs.getString("cpf_cliente")});
            } while (conecta.rs.next());
        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Cliente não Cadastrado! Verifique se o nome do cliente esta correto!");
        } finally {

            conecta.desconecta();
        }

        ModeloTabela modelo = new ModeloTabela(dados, Colunas);

        TabelaCliente.setModel(modelo);
        TabelaCliente.getColumnModel().getColumn(0).setPreferredWidth(40); // ID
        TabelaCliente.getColumnModel().getColumn(0).setResizable(false);
        TabelaCliente.getColumnModel().getColumn(1).setPreferredWidth(200); // Nome Cliente
        TabelaCliente.getColumnModel().getColumn(1).setResizable(false);
        TabelaCliente.getColumnModel().getColumn(2).setPreferredWidth(148); // CNPJ CPF
        TabelaCliente.getColumnModel().getColumn(2).setResizable(false);

        TabelaCliente.getTableHeader().setReorderingAllowed(false);
        TabelaCliente.setAutoResizeMode(TabelaCliente.AUTO_RESIZE_OFF);
        TabelaCliente.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        TabelaCliente.setRowHeight(28);

    }

